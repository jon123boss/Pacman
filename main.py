# Function to draw the maze
def draw_maze(screen, maze):
    for y, row in enumerate(maze):
        for x, cell in enumerate(row):
            if cell == 1:
                pygame.draw.rect(screen, WALL_COLOR, (x * CELL_SIZE, y * CELL_SIZE, CELL_SIZE, CELL_SIZE))

# Function to draw Pac-Man
def draw_pacman(screen, x, y):
    pygame.draw.circle(screen, PACMAN_COLOR, (x, y), PACMAN_RADIUS)
    # Draw mouth
    mouth_rect = pygame.Rect(x, y, PACMAN_RADIUS, PACMAN_RADIUS)
    pygame.draw.polygon(screen, BACKGROUND_COLOR, [(x, y), (x + PACMAN_RADIUS, y - PACMAN_RADIUS // 2), (x + PACMAN_RADIUS, y + PACMAN_RADIUS // 2)])

# Initial Pac-Man position
pacman_x = CELL_SIZE * 1.5
pacman_y = CELL_SIZE * 1.5

# Main game loop
running = True
while running:
    # Event handling
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False

    # Movement controls
    keys = pygame.key.get_pressed()
    if keys[pygame.K_LEFT]:
        pacman_x -= PACMAN_SPEED
    if keys[pygame.K_RIGHT]:
        pacman_x += PACMAN_SPEED
    if keys[pygame.K_UP]:
        pacman_y -= PACMAN_SPEED
    if keys[pygame.K_DOWN]:
        pacman_y += PACMAN_SPEED

    # Fill the background
    screen.fill(BACKGROUND_COLOR)

    # Draw the maze
    draw_maze(screen, maze)

    # Draw Pac-Man
    draw_pacman(screen, int(pacman_x), int(pacman_y))

    # Update the display
    pygame.display.flip()

# Quit Pygame
pygame.quit()
sys.exit()
